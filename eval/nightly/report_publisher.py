"""
Report Publisher
Writes the morning-brief and all structured repair artifacts to the session directory.

Outputs (all in logs/nightly/<session>/):
  00_morning_brief.md    — executive summary for the operator
  01_repair_backlog.md   — machine-readable + human repair list
  02_latency_hotspots.json
  03_dead_zones.json     — intents with 0 coverage
  04_safety_findings.json
  regression.md          — generated by regression_compare.py
  failure_clusters.json  — generated by failure_cluster.py
"""

import json
from datetime import datetime, timezone
from pathlib import Path


BASELINE_RATE = 0.821  # post-NHR combined baseline from 2026-06-16


def publish_report(session_dir: Path, all_results: dict, log_dir: Path) -> dict:
    _write_latency_hotspots(all_results, session_dir)
    _write_dead_zones(all_results, session_dir)
    _write_safety_findings(all_results, session_dir)
    _write_repair_backlog(all_results, session_dir)
    _write_morning_brief(all_results, session_dir)

    return {
        "session_dir": str(session_dir),
        "files": [str(p.name) for p in session_dir.iterdir() if p.suffix in (".md", ".json")],
    }


# ---------------------------------------------------------------------------
# Latency hotspots
# ---------------------------------------------------------------------------

def _write_latency_hotspots(all_results: dict, session_dir: Path):
    nlu = all_results.get("nlu_eval", {})
    results_path = Path(nlu.get("path", ""))
    hotspots = []

    if results_path.exists():
        rows = []
        with open(results_path) as f:
            for line in f:
                try:
                    rows.append(json.loads(line))
                except json.JSONDecodeError:
                    continue

        # Find slowest 20 prompts
        timed = [(r.get("latency_ms", 0), r) for r in rows if r.get("latency_ms")]
        timed.sort(key=lambda x: -x[0])
        hotspots = [
            {
                "rank": i + 1,
                "latency_ms": t,
                "prompt": r.get("prompt"),
                "expected_intent": r.get("expected_intent"),
                "predicted_intent": r.get("predicted_intent", r.get("got")),
                "result": r.get("result"),
            }
            for i, (t, r) in enumerate(timed[:20])
        ]

        # P95 latency
        if timed:
            p95_idx = int(len(timed) * 0.05)
            p95 = timed[p95_idx][0] if p95_idx < len(timed) else timed[-1][0]
        else:
            p95 = None
    else:
        p95 = None

    out = {"p95_latency_ms": p95, "slowest_prompts": hotspots}
    (session_dir / "02_latency_hotspots.json").write_text(json.dumps(out, indent=2))


# ---------------------------------------------------------------------------
# Dead zones
# ---------------------------------------------------------------------------

def _write_dead_zones(all_results: dict, session_dir: Path):
    nlu = all_results.get("nlu_eval", {})
    results_path = Path(nlu.get("path", ""))

    intent_counts: dict[str, dict] = {}

    if results_path.exists():
        with open(results_path) as f:
            for line in f:
                try:
                    row = json.loads(line)
                    intent = row.get("expected_intent", "unknown")
                    if intent not in intent_counts:
                        intent_counts[intent] = {"pass": 0, "fail": 0}
                    if row.get("result") == "pass":
                        intent_counts[intent]["pass"] += 1
                    else:
                        intent_counts[intent]["fail"] += 1
                except json.JSONDecodeError:
                    continue

    dead_zones = []
    low_coverage = []
    for intent, counts in intent_counts.items():
        total = counts["pass"] + counts["fail"]
        rate = counts["pass"] / total if total > 0 else 0.0
        if rate == 0.0 and total > 0:
            dead_zones.append({"intent": intent, "total_scenarios": total, "pass_rate": 0.0})
        elif rate < 0.5:
            low_coverage.append({"intent": intent, "total_scenarios": total, "pass_rate": rate})

    out = {
        "dead_zones": sorted(dead_zones, key=lambda x: -x["total_scenarios"]),
        "low_coverage": sorted(low_coverage, key=lambda x: x["pass_rate"]),
    }
    (session_dir / "03_dead_zones.json").write_text(json.dumps(out, indent=2))


# ---------------------------------------------------------------------------
# Safety findings
# ---------------------------------------------------------------------------

def _write_safety_findings(all_results: dict, session_dir: Path):
    giskard = all_results.get("giskard", {})
    deepeval = all_results.get("deepeval", {})

    breaches = deepeval.get("safety_breach_details", [])
    giskard_issues = giskard.get("issues", [])

    out = {
        "summary": {
            "total_safety_probes": all_results.get("nlu_eval", {}).get("safety_probes", 0),
            "safety_breaches_deepeval": len(breaches),
            "giskard_issues": len(giskard_issues),
        },
        "breaches": breaches,
        "giskard_findings": giskard_issues,
    }
    (session_dir / "04_safety_findings.json").write_text(json.dumps(out, indent=2))


