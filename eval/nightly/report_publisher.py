def _write_morning_brief(all_results: dict, session_dir: Path):
    nlu = all_results.get("nlu_eval", {})
    regression = all_results.get("regression", {})
    clusters = all_results.get("failure_clusters", {})
    deepeval = all_results.get("deepeval", {})
    giskard = all_results.get("giskard", {})
    playwright = all_results.get("playwright", {})
    k6 = all_results.get("k6", {})
    elapsed = all_results.get("elapsed_total", 0)
    session = all_results.get("session_id", session_dir.name)

    rate = nlu.get("rate")
    prev_rate = regression.get("previous_rate")
    delta = regression.get("delta")
    trend = regression.get("trend", "unknown")

    def r(v):
        return f"{v:.1%}" if v is not None else "N/A"

    trend_icon = {"regression": "🔴", "improvement": "🟢", "stable": "🟡", "slight_change": "🟡"}.get(trend, "❓")

    lines = [
        f"# Adwi Morning Brief — {datetime.now().strftime('%Y-%m-%d')}",
        "",
        f"> Session `{session}` · Runtime: {elapsed:.0f}s",
        "",
        "## NLU Health",
        "",
        f"| Metric | Value |",
        f"|--------|-------|",
        f"| Tonight pass rate | **{r(rate)}** |",
        f"| Previous night | {r(prev_rate)} |",
        f"| Delta | {f'{delta:+.1%}' if delta is not None else 'N/A'} {trend_icon} |",
        f"| Trend | {trend} |",
        f"| Total scenarios | {nlu.get('passed', 0) + nlu.get('failed', 0)} |",
        f"| Passed | {nlu.get('passed', 0)} |",
        f"| Failed | {nlu.get('failed', 0)} |",
        "",
        "## Tool Results",
        "",
        f"| Tool | Status |",
        f"|------|--------|",
        f"| DeepEval | {'✅ ' + str(deepeval.get('scored', 0)) + ' scored' if not deepeval.get('skipped') else '⏭️ skipped'} |",
        f"| Giskard | {'✅ ' + str(giskard.get('issues_found', 0)) + ' issues' if not giskard.get('skipped') else '⏭️ skipped'} |",
        f"| Playwright | {'✅ exit ' + str(playwright.get('exit_code', '?')) if not playwright.get('skipped') else '⏭️ skipped'} |",
        f"| k6 | {'✅ exit ' + str(k6.get('exit_code', '?')) if not k6.get('skipped') else '⏭️ skipped'} |",
        "",
        "## Top Failure Clusters",
        "",
    ]

    for i, cluster in enumerate(clusters.get("clusters", [])[:5]):
        lines.append(f"{i+1}. **{cluster['label']}** — {cluster['size']} failures")
        for prompt in cluster.get("sample_prompts", [])[:2]:
            lines.append(f"   - `{prompt}`")
    lines.append("")

    # Repair priorities
    repair_file = session_dir / "01_repair_backlog.json"
    if repair_file.exists():
        try:
            repair_data = json.loads(repair_file.read_text())
            items = repair_data.get("items", [])[:5]
            lines += ["## Top Repair Priorities", ""]
            for item in items:
                human = " ⚠️" if item.get("requires_human") else ""
                lines.append(f"- **#{item['priority']}** {item['suggested_action']}{human}")
            lines.append("")
        except Exception:
            pass

    # Safety
    safety_breaches = deepeval.get("safety_breaches", 0)
    giskard_issues = giskard.get("issues_found", 0)
    if safety_breaches > 0 or giskard_issues > 0:
        lines += [
            "## ⚠️ Safety Alerts",
            "",
            f"- DeepEval detected **{safety_breaches}** safety breaches",
            f"- Giskard found **{giskard_issues}** adversarial vulnerabilities",
            "",
            "> See `04_safety_findings.json` for details. Human review required.",
            "",
        ]

    lines += [
        "---",
        "",
        "## Artifacts in this session",
        "",
        "| File | Contents |",
        "|------|----------|",
        "| `nlu_results.jsonl` | Per-scenario eval results |",
        "| `01_repair_backlog.md` | Prioritized fix list |",
        "| `02_latency_hotspots.json` | Slowest 20 prompts |",
        "| `03_dead_zones.json` | Intents with 0% coverage |",
        "| `04_safety_findings.json` | Safety probe results |",
        "| `failure_clusters.json` | Failure groupings |",
        "| `regression.md` | Delta vs previous nights |",
        "",
        "Run `python3 eval/nightly/orchestrator.py` to start the next nightly session.",
        "Ask Claude: _'Based on logs/nightly/latest/01_repair_backlog.md, fix Adwi'_",
    ]

    (session_dir / "00_morning_brief.md").write_text("\n".join(lines))
