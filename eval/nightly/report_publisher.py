# Repair backlog
# ---------------------------------------------------------------------------

def _write_repair_backlog(all_results: dict, session_dir: Path):
    clusters = all_results.get("failure_clusters", {})
    regression = all_results.get("regression", {})
    nlu = all_results.get("nlu_eval", {})

    repair_items = []

    # From failure cluster repair hints
    for hint in clusters.get("repair_hints", []):
        repair_items.append({
            "priority": len(repair_items) + 1,
            "type": "nlu_routing",
            "expected_intent": hint["expected"],
            "confused_with": hint["most_confused_with"],
            "affected_count": hint["affected_count"],
            "suggested_action": hint["suggested_action"],
            "source": "failure_cluster",
            "requires_human": False,
        })

    # From dead zones
    dead_file = session_dir / "03_dead_zones.json"
    if dead_file.exists():
        dead_data = json.loads(dead_file.read_text())
        for dz in dead_data.get("dead_zones", [])[:5]:
            repair_items.append({
                "priority": len(repair_items) + 1,
                "type": "dead_zone",
                "expected_intent": dz["intent"],
                "confused_with": None,
                "affected_count": dz["total_scenarios"],
                "suggested_action": f"Add regex + INTENT_SYSTEM entry for '{dz['intent']}'",
                "source": "dead_zone_analysis",
                "requires_human": False,
            })

    # From Giskard adversarial findings
    giskard = all_results.get("giskard", {})
    for issue in giskard.get("issues", [])[:5]:
        repair_items.append({
            "priority": len(repair_items) + 1,
            "type": "adversarial_vulnerability",
            "expected_intent": "__none__",
            "confused_with": issue.get("predicted_intent"),
            "affected_count": 1,
            "suggested_action": issue.get("description", "Review blocked-path handling"),
            "source": "giskard_scan",
            "requires_human": True,
        })

    # Category regressions
    for cr in regression.get("category_regression", [])[:3]:
        repair_items.append({
            "priority": len(repair_items) + 1,
            "type": "regression",
            "expected_intent": cr["category"],
            "confused_with": None,
            "affected_count": cr["change"],
            "suggested_action": f"Regression in '{cr['category']}': +{cr['change']} new failures vs yesterday",
            "source": "regression_compare",
            "requires_human": cr["change"] > 10,
        })

    out = {
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "session": session_dir.name,
        "nlu_pass_rate": nlu.get("rate"),
        "baseline_rate": BASELINE_RATE,
        "delta_vs_baseline": (nlu.get("rate", 0) - BASELINE_RATE) if nlu.get("rate") else None,
        "total_items": len(repair_items),
        "human_review_required": sum(1 for r in repair_items if r.get("requires_human")),
        "items": repair_items,
    }
    (session_dir / "01_repair_backlog.md").write_text(_repair_backlog_md(out))
    (session_dir / "01_repair_backlog.json").write_text(json.dumps(out, indent=2))


def _repair_backlog_md(data: dict) -> str:
    rate = data.get("nlu_pass_rate")
    baseline = data.get("baseline_rate", BASELINE_RATE)
    delta = data.get("delta_vs_baseline")

    def r(v):
        return f"{v:.1%}" if v is not None else "N/A"

    lines = [
        "# Repair Backlog — Nightly Eval\n",
        f"**Session:** {data['session']}  ",
        f"**Generated:** {data['generated_at'][:19]}  ",
        f"**NLU pass rate:** {r(rate)} (baseline: {r(baseline)}, delta: {f'{delta:+.1%}' if delta else 'N/A'})  ",
        f"**Total items:** {data['total_items']} ({data['human_review_required']} require human review)",
        "",
        "---",
        "",
        "## Items (by priority)\n",
    ]

    for item in data.get("items", []):
        requires = "⚠️ HUMAN REVIEW" if item.get("requires_human") else "🤖 Auto-repair candidate"
        lines += [
            f"### #{item['priority']} — {item['type'].upper()} {requires}",
            f"**Intent:** `{item['expected_intent']}`"
            + (f" → confused with `{item['confused_with']}`" if item.get("confused_with") else ""),
            f"**Affected:** {item['affected_count']} scenarios  ",
            f"**Source:** {item['source']}  ",
            f"**Action:** {item['suggested_action']}",
            "",
        ]

    lines += [
        "---",
        "",
        "## How to apply these fixes",
        "",
        "1. Read each item above.",
        "2. Open `adwi/adwi_cli.py` → find `_REGEX_INTENTS` (~line 503).",
        "3. Add patterns BEFORE the intent they must beat.",
        "4. Run: `python3 -m py_compile adwi/adwi_cli.py && echo OK`",
        "5. Run eval: `python3 logs/simeval/run_large_eval.py --workers 5`",
        "6. Compare to baseline: **82.1%**",
        "7. Mark item as applied in this file.",
        "",
        "> Items marked ⚠️ HUMAN REVIEW must not be auto-patched.",
        "> They touch safety, security, or high-blast-radius routing.",
    ]
    return "\n".join(lines)


